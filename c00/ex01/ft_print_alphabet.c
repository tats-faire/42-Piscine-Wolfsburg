#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

/* Here no parameter because it doesn't need any input I think */
void	ft_print_alphabet(void)
{
	char	c;

	c = 'a';
	while (c <= 'z')
	{
		ft_putchar(c);
		c++;
	}
}

